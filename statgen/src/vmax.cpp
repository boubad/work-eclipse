bool UnivarStatDesc::get_vmax(size_t n, double &res, double alpha /*= 0.05 */) {
	if ((n < 2) || (alpha < 0.0) || (alpha > 1.0)) {
		return (false);
	}
	double qx = 200.0 * alpha;
	size_t nx = sizeof(TAB_XVALS) / sizeof(size_t);
	size_t ny = sizeof(TAB_YVALS) / sizeof(double);
	const size_t *px = &(TAB_XVALS[0]);
	const double *py = &(TAB_YVALS[0]);
	const double *pz = &(TAB_ZVALS[0]);
	return (UnivarStatDesc::compute_value(nx, ny, px, py, pz, n, qx, res));
} // get_vmax
